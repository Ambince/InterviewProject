package com.amence.thoughtworks.bean;

/**
 * 动物实体工具类
 * @author Amence
 *
 */
public class Animal {
	private String name;
	
	private String time;
	
	private String point;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getTime() {
		return time;
	}
	public void setTime(String time) {
		this.time = time;
	}
	public String getPoint() {
		return point;
	}
	public void setPoint(String point) {
		this.point = point;
	}
	

}
